    private void populateTable() {
         DefaultTableModel mod = (DefaultTableModel)table.getModel();
         mod.setRowCount(0);
         for(Employee emp:emphistory.getHistory())
         {
             Object[] row = new Object[10];
             row[0]=emp;
             row[1] = emp.getId();
             row[2] = emp.getAge();
             row[3]=emp.getGender();
             row[4] = emp.getLevel();
             row[5] = emp.getTeam_info();
            row[6] = emp.getPos_title();
            row[7] = emp.getDate();
            row[9]=emp.getImage();
            
             mod.addRow(row);
             
         }
    }

    
